    private void agregarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarButtonActionPerformed
        if (!modoAgregar) {
            modoAgregar = true;
            agregarButton.setText("Confirmar");

            nombreTextBox.setText("");
            usuarioTextBox.setText("");
            rolTextBox.setText("");

            nombreTextBox.setEnabled(true);
            usuarioTextBox.setEnabled(true);
            rolTextBox.setEnabled(true);
        } else {
            try {
                Persona persona = new Persona(nombreTextBox.getText());
                Rol rol = new Rol(rolTextBox.getText());

                Usuario usuario = new Usuario(
                        usuarioTextBox.getText(),
                        persona,
                        rol
                );

                usuarioDAO.agregarUsuario(usuario);
                JOptionPane.showMessageDialog(this, "Usuario agregado correctamente");

                modoAgregar = false;
                agregarButton.setText("Agregar");
                inicializarEstado();

            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Error al agregar: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_agregarButtonActionPerformed

    private void eliminarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarButtonActionPerformed
        try {
            int confirmacion = JOptionPane.showConfirmDialog(this, "¿Seguro que desea eliminar este usuario?", "Confirmar", JOptionPane.YES_NO_OPTION);
            
            if (confirmacion == JOptionPane.YES_OPTION) {
                usuarioDAO.eliminarUsuario(usuarioIdActual);

                nombreTextBox.setText("");
                usuarioTextBox.setText("");
                rolTextBox.setText("");

                JOptionPane.showMessageDialog(this, "Usuario eliminado correctamente");
                inicializarEstado();
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_eliminarButtonActionPerformed


