    private void buscarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarButtonActionPerformed
        try {
            Usuario usuario = usuarioDAO.buscarUsuario(buscarUsuarioTextBox.getText());

            if (usuario != null) {
                usuarioIdActual = usuario.getIdUsuario();
                nombreTextBox.setText(usuario.getPersona().getNombre());
                usuarioTextBox.setText(usuario.getUsername());
                rolTextBox.setText(usuario.getRol().getNombreRol());
            } else {
                JOptionPane.showMessageDialog(this, "Usuario no encontrado");
            }

            inicializarEstado();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al buscar: " + e.getMessage());
        }
    }//GEN-LAST:event_buscarButtonActionPerformed

